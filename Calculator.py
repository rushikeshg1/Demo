def add(x, y):
    return x + y

def subtract(x ,y):
    return x - y

def multiply(x, y):
    return x * y

def divide(x ,y):
    return x / y

num1 = 20
num2 = 10

print("Addition = ", num1, "+", num2, "=", add(num1, num2))
print("Subtraction = ", num1, "-", num2, "=", subtract(num1, num2))
print("Multiplication = ", num1, "*", num2, "=", multiply(num1, num2))
print("Division = ", num1, "/", num2, "=", divide(num1, num2))